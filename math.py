print("# `//` will round down to the nearest int")
print("# `%` (modulo) used to represent remainder")

print(f'\n2 ** 3 \n  # prints: {2 ** 3}')
print(f'\n3 // 4   \n# prints: {3 // 4}')
print(f'\n5 // 4   \n# prints: {5 // 4}')
